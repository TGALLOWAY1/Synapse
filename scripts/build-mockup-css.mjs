#!/usr/bin/env node
// Compile the mockup-render Tailwind stylesheet and wrap it in a TS module.
//
// Why a TS wrapper instead of a raw .css import? Vite's CSS pipeline
// intercepts `.css` imports (including `?raw` and `?inline`) and returns an
// HMR-friendly object whose default content is empty under Vitest. Emitting
// a plain `.ts` module with the CSS as a default-exported template literal
// sidesteps the pipeline entirely so the iframe srcDoc always carries the
// precompiled utility set.

import { execFileSync } from 'node:child_process';
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const configPath = path.join(repoRoot, 'scripts', 'mockup-tailwind.config.cjs');
const inputPath = path.join(repoRoot, 'src', 'styles', 'mockup-tailwind.input.css');
const cssOutPath = path.join(repoRoot, 'src', 'styles', 'mockup-tailwind.generated.css');
const tsOutPath = path.join(repoRoot, 'src', 'styles', 'mockup-tailwind.generated.ts');

const tailwindBin = path.join(repoRoot, 'node_modules', '.bin', 'tailwindcss');

console.log('[mockup-css] compiling Tailwind utilities…');
execFileSync(
    tailwindBin,
    ['-c', configPath, '-i', inputPath, '-o', cssOutPath, '--minify'],
    { stdio: 'inherit', cwd: repoRoot },
);

const css = await fs.readFile(cssOutPath, 'utf8');

// Escape backslash, backtick, and ${ for a JS template literal.
const escaped = css
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');

const banner = `// AUTOGENERATED by scripts/build-mockup-css.mjs from
// scripts/mockup-tailwind.config.cjs. Do not edit by hand — run
// \`npm run mockup-css:build\` after expanding the safelist.

`;

const module = `${banner}const mockupTailwindCss = \`${escaped}\`;\nexport default mockupTailwindCss;\n`;

await fs.writeFile(tsOutPath, module, 'utf8');
console.log(`[mockup-css] wrote ${path.relative(repoRoot, tsOutPath)} (${(module.length / 1024).toFixed(1)} KB)`);
